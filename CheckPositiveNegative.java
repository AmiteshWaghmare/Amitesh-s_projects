public class CheckPositiveNegative {

    public static void main(String[] args){

        int num = -1;

        if(num > 0){

            System.out.println(num + " Number is positive");
        }
        else if(num < 0){

            System.out.println(num + " Number is negative");
        }
        else{

            System.out.println(num + " Number is zero");
        }
        
    }
    
}
